import csv

# CSV 파일 경로
file_path = 'busanJinCCTV.csv'

total_cameras = 0

# CSV 파일을 열고 읽기
with open(file_path, mode='r', encoding='cp949') as file:
    # CSV 파일을 읽기 위한 reader 객체 생성
    csv_reader = csv.reader(file)

    # 첫 번째 행은 헤더이므로 건너뛰기
