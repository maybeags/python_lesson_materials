from prettytable import PrettyTable

'''
PrettyTable 공식 문서
PrettyTable의 공식 문서는 PyPI 페이지와 GitHub 저장소에서 찾을 수 있습니다.

field_names
이 메서드는 테이블의 필드 이름(열 헤더)을 설정하거나 가져오는 데 사용됩니다.

사용법:

python
코드 복사
table = PrettyTable()
table.field_names = ["필드1", "필드2", "필드3"]
설명:

필드 이름 설정: field_names에 문자열 리스트를 할당하여 테이블의 헤더를 정의할 수 있습니다.
필드 이름 가져오기: field_names를 액세스하면 현재 헤더를 가져올 수 있습니다.
add_row
이 메서드는 테이블에 단일 행을 추가하는 데 사용됩니다.

사용법:

python
코드 복사
table.add_row(["값1", "값2", "값3"])
설명:

행 추가: field_names에 정의된 각 필드에 해당하는 값 리스트를 제공해야 합니다. 
리스트의 값 개수는 필드 이름의 개수와 일치해야 합니다.
'''

# 포켓몬 데이터를 리스트로 정의합니다. (한국어 이름으로 변경)
pokemon_data = [
    (1, "이상해씨", "풀/독"),
    (2, "이상해풀", "풀/독"),
    (3, "이상해꽃", "풀/독"),
    (4, "파이리", "불꽃"),
    (5, "리자드", "불꽃"),
    (6, "리자몽", "불꽃/비행"),
    (7, "꼬부기", "물"),
    (8, "어니부기", "물"),
    (9, "거북왕", "물"),
    (10, "캐터피", "벌레"),
    (11, "단데기", "벌레"),
    (12, "버터플", "벌레/비행"),
    (13, "뿔충이", "벌레/독"),
    (14, "딱충이", "벌레/독"),
    (15, "독침붕", "벌레/독"),
    (16, "구구", "노말/비행"),
    (17, "피죤", "노말/비행"),
    (18, "피죤투", "노말/비행"),
    (19, "꼬렛", "노말"),
    (20, "레트라", "노말"),
]

# PrettyTable 객체를 생성합니다.
table = PrettyTable()

# 테이블의 열(header)을 정의합니다.
table.field_names = ["번호", "이름", "속성"]

# 포켓몬 데이터를 테이블에 추가합니다.
for pokemon in pokemon_data:
    table.add_row(pokemon)

# 테이블을 출력합니다.
print(table)

table2 = PrettyTable()

table2.field_names = ["번호", "이름", "속성"]

# 포켓몬 데이터를 하나씩 테이블에 추가합니다.
table2.add_row((1, "이상해씨", "풀/독"))
table2.add_row((2, "이상해풀", "풀/독"))
table2.add_row((3, "이상해꽃", "풀/독"))
table2.add_row((4, "파이리", "불꽃"))
table2.add_row((5, "리자드", "불꽃"))
table2.add_row((6, "리자몽", "불꽃/비행"))

# 테이블을 출력합니다.
print(table2)